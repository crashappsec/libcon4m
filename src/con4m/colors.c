color_t
lookup_color(char *name) {
    // For now, we'll just linear search.  Later, binary search.

    for(int i = 0; color_data[i].name != NULL; i++) {
	if (!strcmp(color_data[i].name, name)) {
	    return color_data[i].rgb;
	}
    }
    return -1;
}


int32_t
to_vga(int32_t truecolor)
{
    int32_t r = (truecolor >> 16) & 0xff;
    int32_t g = (truecolor >> 8)  & 0xff;
    int32_t b = (truecolor >> 0)  & 0xff;

    return ((int32_t)(r * 7 / 255) << 5) |
	   ((int32_t)(g * 7 / 255) << 2) |
           ((int32_t)(b * 3 / 255));
}
